

let puntaje = 5;

console.log(puntaje++);                 /*Primero imprime el puntaje y después lo suma. Por eso en este caso nos imprimirá "5", 
                                        anque después de imprimirlo le suma 1*/

console.log(++puntaje);                 /*Ahora puntaje vale 6. Y como el operador esta antes que la variable, primero lo suma y 
                                        después lo imprime, por lo que ahora si que imprimirá el valor final, que es 7 */


puntaje++;                              //Ahora primero le incrementamos y después lo imprimimos. Por lo que nos dará el resultado final.
console.log(puntaje);




puntaje--;                              //En este caso igual que el anterior pero restando.
console.log(puntaje);


puntaje+=3;                             //Le suma 3 al puntaje
console.log(puntaje);





